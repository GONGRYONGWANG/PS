vector<pii> E[100001];
int visited[100001];

void solve() {
    
    int N, M;
    cin >> N >> M;
    int s, e;
    cin >> s >> e;
    for (int i = 0; i < M; i++) {
        int u, v, w;
        cin >> u >> v >> w;
        E[u].push_back({ v,w });
        E[v].push_back({ u,w });
    }

    pq<pii> q; // capacity, node
    q.push({ inf,s });
    while (!q.empty()) {
        int cur = q.top().second;
        if (visited[cur]) {
            q.pop();
            continue;
        }
        visited[cur] = q.top().first;
        q.pop();
        for (int i = 0; i < E[cur].size(); i++) {
            int nx = E[cur][i].first;
            if (visited[nx]) continue;
            q.push({min(visited[cur], E[cur][i].second) ,nx });
        }
    }
    cout << visited[e];



}

