void solve() {
    int N, M;
    cin >> N >> M;
    vector<pll> arr(N);
    for (int i = 0; i < N; i++) {
        cin >> arr[i].second >> arr[i].first;
    }
    sort(arr.begin(), arr.end());
    arr.push_back({ M+1,M+2 });
    ll ans = M;
    ll l, r;
    l = r = 0;
    for (int i = 0; i <= N; i++) {
        if (arr[i].first >= arr[i].second) continue;
        if (arr[i].first <= r) {
            r = max(r, arr[i].second);
        }
        else {
            ans += 2*(r - l);
            l = arr[i].first;
            r = arr[i].second;
        }
    }
    cout << ans;
}

